public class Player {
    int numberOfAtBats;
    int numberOfBasesEarned;
    float battingAverage;
    float sluggingPercentage;

    public Player(int numberOfAtBats, int numberOfBasesEarned, float battingAverage, float sluggingPercentage) {
        this.numberOfAtBats = numberOfAtBats;
        this.numberOfBasesEarned = numberOfBasesEarned;
        this.battingAverage = battingAverage;
        this.sluggingPercentage = sluggingPercentage;
    }


}
